<template>
    <div>
        <Myproduit :produits="produits"/>
    </div>
</template>

<script>
    import Myproduit from "../components/myproduit";


    export default {
        name: "produit",
        data(){
            return{
                produits:{}
            }
        },
        components:{
            Myproduit
        },
        created() {
            this.axios.get("http://localhost:3000/produit/all")
                .then(res => {
                    this.produits = res.data.produit
                })
                .catch(err => {
                    alert(err)
                })
        }
    }
</script>

